// الحصول على المقال التالي
function getNextArticle(currentId) {
    const articleIds = Object.keys(articlesData).map(id => parseInt(id));
    const currentIndex = articleIds.indexOf(currentId);
    const nextIndex = (currentIndex + 1) % articleIds.length;
    return articlesData[articleIds[nextIndex]];
}

// الحصول على المقال السابق
function getPreviousArticle(currentId) {
    const articleIds = Object.keys(articlesData).map(id => parseInt(id));
    const currentIndex = articleIds.indexOf(currentId);
    const prevIndex = currentIndex === 0 ? articleIds.length - 1 : currentIndex - 1;
    return articlesData[articleIds[prevIndex]];
}

